def tr(state, key: str, **kwargs) -> str:
    lang = getattr(state, 'language', 'ja')
    table = TRANSLATIONS.get(lang, TRANSLATIONS['ja'])
    text = table.get(key) or TRANSLATIONS['ja'].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text
